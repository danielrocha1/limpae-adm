import { readdirSync, readFileSync, mkdirSync, writeFileSync } from "node:fs";
import { join, relative } from "node:path";

const root = process.cwd();
const modelsDir = join(root, "go");
const outputFile = join(root, "src", "generated", "modelSchema.js");

const scalarTypes = new Set([
  "bool",
  "byte",
  "float32",
  "float64",
  "int",
  "int8",
  "int16",
  "int32",
  "int64",
  "rune",
  "string",
  "time.Time",
  "uint",
  "uint8",
  "uint16",
  "uint32",
  "uint64",
]);

function walk(dir) {
  return readdirSync(dir, { withFileTypes: true }).flatMap((entry) => {
    const path = join(dir, entry.name);
    if (entry.isDirectory()) return walk(path);
    return entry.isFile() && entry.name.endsWith(".go") ? [path] : [];
  });
}

function parseTags(tag = "") {
  const tags = {};
  const tagPattern = /(\w+):"([^"]*)"/g;
  let match;
  while ((match = tagPattern.exec(tag))) {
    tags[match[1]] = match[2];
  }
  return tags;
}

function cleanType(type) {
  return type.replace(/^\[\]/, "").replace(/^\*/, "");
}

function inputKind(type, name) {
  const normalized = cleanType(type);
  if (normalized === "bool") return "checkbox";
  if (/^(int|uint|float)/.test(normalized)) return "number";
  if (normalized === "time.Time") return "datetime";
  if (/email/i.test(name)) return "email";
  if (/photo|image|avatar/i.test(name)) return "image";
  if (/password/i.test(name)) return "password";
  return "text";
}

function parseStructs(filePath) {
  const source = readFileSync(filePath, "utf8");
  const structs = [];
  const structPattern = /type\s+(\w+)\s+struct\s*\{([\s\S]*?)\n\}/g;
  let structMatch;

  while ((structMatch = structPattern.exec(source))) {
    const [, name, body] = structMatch;
    const fields = body
      .split(/\r?\n/)
      .map((line) => line.replace(/\/\/.*$/, "").trim())
      .filter(Boolean)
      .map((line) => {
        const match = line.match(/^(\w+)\s+([^\s`]+)(?:\s+(`[^`]+`))?/);
        if (!match) return null;
        const [, fieldName, goType, rawTag] = match;
        const tags = parseTags(rawTag);
        const baseType = cleanType(goType);
        const isArray = goType.startsWith("[]");
        const isPointer = goType.startsWith("*");
        const isRelation = isArray || (!scalarTypes.has(baseType) && !baseType.startsWith("datatypes."));

        return {
          name: fieldName,
          goType,
          baseType,
          isArray,
          isPointer,
          isRelation,
          tags,
          input: inputKind(goType, fieldName),
          editable: !isRelation && !["ID", "CreatedAt", "EmailVerifiedAt", "PasswordHash"].includes(fieldName),
          table: !isRelation && !["PasswordHash", "EmailVerifiedAt"].includes(fieldName),
          sensitive: /password|hash/i.test(fieldName),
        };
      })
      .filter(Boolean);

    structs.push({
      name,
      source: relative(root, filePath).replaceAll("\\", "/"),
      fields,
    });
  }

  return structs;
}

const structs = walk(modelsDir).flatMap(parseStructs).sort((a, b) => a.name.localeCompare(b.name));
const generated = `// Auto-generated by scripts/generate-model-schema.mjs. Do not edit by hand.
export const modelSchemas = ${JSON.stringify(structs, null, 2)};

export const userSchema = modelSchemas.find((schema) => schema.name === "User");
export const clientProfileSchema = modelSchemas.find((schema) => schema.name === "UserProfile");
export const diaristProfileSchema = modelSchemas.find((schema) => schema.name === "Diarists");
`;

mkdirSync(join(root, "src", "generated"), { recursive: true });
writeFileSync(outputFile, generated);
console.log(`Generated ${relative(root, outputFile)} from ${structs.length} structs.`);
